                'type' => 'artisan',
                'description' => 'Publish config',
                'command' => 'vendor:publish',
                'params' => [
                    '--provider' => 'InetStudio\PagesPackage\Pages\Providers\ServiceProvider',
                    '--tag' => 'config',
                ],
            ],
